// Helper: get all province names
export function getAllProvinces(): string[] {
  return regions.flatMap((r) => r.provinces.map((p) => p.name));
}

// Helper: get province by name
export function findProvince(name: string): Province | undefined {
  for (const region of regions) {
    const found = region.provinces.find((p) => p.name === name);
    if (found) return found;
  }
  return undefined;
}

// Helper: get region by province name
export function findRegionByProvince(provinceName: string): Region | undefined {
  return regions.find((r) => r.provinces.some((p) => p.name === provinceName));
}
